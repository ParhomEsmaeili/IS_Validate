    parser.add_argument('-np', type=int, required=False, default=8,
                        help=f'Number of processes used. Default: 1')
    args = parser.parse_args()
    corrupt_dataset(args.d_path, args.dataset_id, args.corruption_method, args.corruption_param, args.process_Ts, args.np)
