def corrupt_nifti(filename, output_folder, corruption_method:str, corruption_param:int):
    supported_corruption = ['ADD_GAUSS', 'MULTIP_GAUSS']
    if corruption_method.upper() not in supported_corruption:
        raise Exception 
    os.makedirs(output_folder, exist_ok=False)

    img_itk = sitk.ReadImage(filename)
    dim = img_itk.GetDimension()
    file_base = os.path.basename(filename)
    
    if not dim == 3:
        raise Exception('Hardcoded to being only for 3D volumes.')
    
    img_npy = sitk.GetArrayFromImage(img_itk)
    spacing = img_itk.GetSpacing()
    origin = img_itk.GetOrigin()
    direction = img_itk.GetDirection()

    if corruption_method.upper() == 'ADD_GAUSS':  
        # print(img_npy.shape)  
        noise = np.random.normal(0, corruption_param, list(img_npy.shape))
        # print(noise.shape)
        noise_img = img_npy + noise 
    
        #Zero centred, Gaussian noise, generated by sampling from a gaussian distribution. Sigma = std of noise generated iid for each voxel.
        #Applied to the image like I(x,y,z) + Norm(x,y,z) 

        #Usually better for CT.
    elif corruption_method.upper() == 'MULTIP_GAUSS':
        noise_img = img_npy * np.random.normal(1, corruption_param, list(img_npy.shape))

        #1 centred, multiplicative factors for noising, generated by sampling from a gaussian distribution with sigma parameter, multip factor is iid for each voxel. 
        # Applied to the image like I(x,y,z) * Noise(x,y,z). Not physically representative in all cases but is quite powerful for quickly destroying structure.
    noise_img = noise_img.astype(np.float32)
    img_itk_new = sitk.GetImageFromArray(noise_img)
    img_itk_new.SetSpacing(spacing)
    img_itk_new.SetOrigin(origin)
    img_itk_new.SetDirection(direction)
    sitk.WriteImage(img_itk_new, os.path.join(output_folder, file_base)) #Hardcoded to have the exact same filename as originally

def corrupt_dataset(source_folder: str, 
                    dataset_id: int,
                    corruption_method: str,
                    corruption_param: str, 
                    process_Ts: bool = False, 
                    num_processes: int = 1) -> None:
    if source_folder.endswith('/') or source_folder.endswith('\\'):
        source_folder = source_folder[:-1] 

    labelsTr_path = os.path.join(source_folder, 'labelsTr')
    imagesTr_path = os.path.join(source_folder, 'imagesTr')
    labelsTs_path = os.path.join(source_folder, 'labelsTs')
    imagesTs_path = os.path.join(source_folder, 'imagesTs')
    
    assert os.path.isdir(labelsTr_path) and os.path.exists(labelsTr_path), f"labelsTr missing in source folder or was not a subdirectory."
    assert os.path.isdir(imagesTr_path) and os.path.exists(imagesTr_path), f"imagesTr missing in source folder or was not a subdirectory."
    if process_Ts:
        assert os.path.isdir(imagesTs_path) and os.path.exists(imagesTs_path), f"imagesTs missing in source folder or was not a subdirectory."
        # assert os.path.isdir(labelsTs_path) and os.path.exists(labelsTs_path), "labelsTs missing in source folder or was not a subdirectory."
    
    dataset_json = os.path.join(source_folder, 'dataset.json')
    assert os.path.isfile(dataset_json), f"Formatted dataset.json was missing in source_folder"


    #we adopted the nnu-net naming convention, so when we noise just use this to inform the name extraction as a temporary hack.
    task, dataset_name = os.path.basename(source_folder).split('_')

    # check if target dataset id is already being used for another dataset
    existing_datasets = check_dataset_existence(datasets_path, dataset_id)
    assert len(existing_datasets) == 0, f"Target dataset id {dataset_id} is already taken, please consider changing " \
                                        f"it. Conflicting dataset: {existing_datasets}"

    target_dataset_name = f"Dataset{dataset_id:03d}CorruptionMethod{corruption_method}CorruptionParam{corruption_param}_{dataset_name}"
    target_folder = os.path.join(datasets_path, target_dataset_name)
    target_imagesTr = os.path.join(target_folder, 'imagesTr')
    target_imagesTs = os.path.join(target_folder, 'imagesTs')
    target_labelsTr = os.path.join(target_folder, 'labelsTr')
    target_labelsTs = os.path.join(target_folder, 'labelsTs')
    
    os.makedirs(target_imagesTr, exist_ok=False)
    #copy the dataset json
    shutil.copy(dataset_json, os.path.join(target_folder, 'dataset.json'))
    shutil.copytree(labelsTr_path, target_labelsTr)

    if process_Ts:
        os.makedirs(target_imagesTs, exist_ok=False)
        try:
            shutil.copytree(labelsTs_path, target_labelsTs)
        except:
            print('no labelsTs in source folder.')

    with multiprocessing.get_context("spawn").Pool(num_processes) as p:
        results = []

        # noise the images
        source_cases = [i for i in extract_subdirs(imagesTr_path, join=False, sort=True) if
                         not i.startswith('.') and not i.startswith('_')]
        #appending the filename to the folder path.
        source_images = [os.path.join(imagesTr_path, case, f'{case}_0000.nii.gz') for case in source_cases]
        
        target_sample_folders = [os.path.join(target_imagesTr, case) for case in source_cases]
        # split_4d_MSD_nifti(source_images[0], target_sample_folders[0])
        corruption_methods = [corruption_method] * len(source_images)
        corruption_params = [float(corruption_param)] * len(source_images)
        results.append(
            p.starmap_async(
                corrupt_nifti, zip(source_images, target_sample_folders, corruption_methods, corruption_params) #[target_imagesTr] * len(source_images))
            )
        )

        # noise the test images
        if process_Ts:
            source_cases = [i for i in extract_subdirs(imagesTs_path, join=False, sort=True) if
                            not i.startswith('.') and not i.startswith('_')]
            source_images = [os.path.join(imagesTs_path, case, f'{case}_0000.nii.gz') for case in source_cases] #hardcoded for single modality/channel imaging data.
            target_sample_folders = [os.path.join(target_imagesTs, case) for case in source_cases]
            # print(target_sample_folders)
            corruption_methods = [corruption_method] * len(source_images)
            corruption_params = [float(corruption_param)] * len(source_images)
            results.append(
                p.starmap_async(
                    corrupt_nifti, zip(source_images, target_sample_folders, corruption_methods, corruption_params)
                )
            )
            
        [i.get() for i in results]

    print('Finished!')
if __name__ == '__main__':
    parser = argparse.ArgumentParser()
